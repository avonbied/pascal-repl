class Operation(object):
    @staticmethod
    def add(x, y):
        return(x + y)
    @staticmethod
    def neg(x):
        return(- x)
    @staticmethod
    def mult(x, y):
        return(x * y)
    @staticmethod
    def div(x, y):
        return(x / y)
    @staticmethod
    def mod(x, y):
        return(x % y)